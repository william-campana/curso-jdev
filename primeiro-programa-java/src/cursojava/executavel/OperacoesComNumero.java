package cursojava.executavel;

public class OperacoesComNumero {

	public static void main(String[] args) {
		
		int nota1 = 90;
		int nota2 = 70;
		int nota3 = 50;
		int nota4 = 88;
		int totalNota = nota1 + nota2 + nota3 + nota4;
		double media = totalNota / 4f;
		
		
		System.out.println("Média das notas: " + media);
				

	}

}
